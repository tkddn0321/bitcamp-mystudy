package study.lang.literal;

// 실습

// - 10진수 literal 100을 지시에 따라 다양한 진수 리터럴로 표현하라
// - 출력
//   100
//   100
//   100
//   100
//   100
//

public class Test02 {
    public static void main(String[] args) {
        // 코드를 완성하라.
        int binary = 0b1100100;
        int octal = 0144;
        int hexadecimal = 0x64;

        System.out.println(100); // 10진수
        System.out.println(binary); // 2진수
        System.out.println(octal); // 8진수
        System.out.println(hexadecimal); // 16진수
    }
}
